KEYSERVER=${KEYSERVER:-"https://keyserver.ubuntu.com"}
OUT_DIR=${OUT_DIR:-"/tmp/gpg-keys/"}

mkdir -p "${OUT_DIR}"

fetch_key() {
  local key="$1"
  local url="${KEYSERVER}/pks/lookup?op=get&search=0x${key}"
  local out="${OUT_DIR}/${key}.asc"

  if command -v curl >/dev/null 2>&1; then
    curl -fsSL "${url}" -o "${out}"
  elif command -v wget >/dev/null 2>&1; then
    wget -qO "${out}" "${url}"
  else
    echo "error: curl or wget required" >&2
    exit 1
  fi
}

for key in "${KEYS[@]}"; do
  echo "fetching ${key}..."
  fetch_key "${key}"
done

echo "importing keys..."
gpg --import "${OUT_DIR}"/*.asc

echo "done"
